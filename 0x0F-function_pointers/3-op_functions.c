#include "3-calc.h"
/**
 * op_add - performs operation
 *
 * @a:first num
 * @b:second num
 *
 * Return: result of calculation
 **/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - performs operation
 *
 * @a:first num
 * @b:second num
 *
 * Return: result of calculation
 **/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - performs operation
 *
 * @a:first num
 * @b:second num
 *
 * Return: result of calculation
 **/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - performs operation
 *
 * @a:first num
 * @b:second num
 *
 * Return: result of calculation
 **/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - performs operation
 *
 * @a:first num
 * @b:second num
 *
 * Return: result of calculation
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
